#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, size_t n)
{
    int i;

    i = 0;
    while(n - i)
    {
        if ((s1 + i) != (s2 + i))
            return (((s1+i) - (s1+i)));
        i++;
    }
    return (0);
                    
}
